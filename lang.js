let currentLang = localStorage.getItem("language") || "th";

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("language", lang);
  document.documentElement.lang = lang;

  for (const key in translations[lang]) {
    const element = document.getElementById(key);
    if (element) {
      const text = translations[lang][key];
      const tag = element.tagName.toLowerCase();

      if (tag === "input" || tag === "textarea") {
        element.placeholder = text;
      } else if (tag === "option") {
        element.label = text; 
        element.textContent = text; 
      } else {
        element.textContent = text;
      }
    }
  }

  const langButton = document.getElementById("langSwitch");
  if (langButton) {
    langButton.innerText = lang === "th" ? "EN" : "TH";
  }
}

function setupLanguageSwitch() {
  const langSwitchButton = document.getElementById("langSwitch");
  if (langSwitchButton) {
    langSwitchButton.addEventListener("click", () => {
      const newLang = currentLang === "th" ? "en" : "th";
      setLanguage(newLang);
    });
  }
}
window.addEventListener("load", () => {
  const defaultLang = localStorage.getItem("language") || "th";
  setLanguage(defaultLang);
  setupLanguageSwitch();
});



